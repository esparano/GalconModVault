            <tr><td><p>by Oberdiah</p>
            <tr><td><input type='button' value='Start' onclick='newmap' />
            <tr><td><p></p>
			<tr><td><p>Number of planets:</p></td>
			<tr><td><input type="text" name="number_of_planets" width="40" /></td>
			<tr><td><p>Maximum Production:</p></td>
			<tr><td><input type="text" name="max_production" width="40" /></td>
			<tr><td><p>Map size: (0.2-10)</p></td>
			<td><p>Lasers?</p></td>
			<tr><td><input type="text" name="size_of_map" width="40" /></td>
			<td><input type="toggle" name="lasers" width="40" /></td>
        </table>
