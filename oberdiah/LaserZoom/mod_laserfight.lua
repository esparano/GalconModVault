function event(e)

	if (e.type == "ui:motion") and GLOBAL.circleshowing == 1 then
		if GLOBAL.mousecircle ~= nil then
			GLOBAL.mousecircle:destroy()
		end
		GLOBAL.mousecircle = g2.new_circle(COLORS[4], e.x, e.y, 20)
	end
	
	if e.type == "ui:down" or e.type == "ui:up" then
		GLOBAL.justclicked = 1
	end
	
    if (e.type == "ui:motion" or e.type == "ui:down") and GLOBAL.zoom ~= OPTIONS.size_of_map + 0.1 then
		if e.x > (OPTIONS.screen_width*GLOBAL.zoom/10*9 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 1
			if e.x > (OPTIONS.screen_width*GLOBAL.zoom/10*9.5 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 2
			end
			if e.x > (OPTIONS.screen_width*GLOBAL.zoom/10*9.99 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 3
			end
		elseif e.x < (OPTIONS.screen_width*GLOBAL.zoom/10 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 4
			if e.x < (OPTIONS.screen_width*GLOBAL.zoom/20 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 5
			end
			if e.x < (OPTIONS.screen_width*GLOBAL.zoom/1000 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
			GLOBAL.movingright = 6
			end
		else
			GLOBAL.movingright = 0
		end

		if e.y > (OPTIONS.screen_height*GLOBAL.zoom/10*9 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 1
			if e.y > (OPTIONS.screen_height*GLOBAL.zoom/10*9.5 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 2
			end
			if e.y > (OPTIONS.screen_height*GLOBAL.zoom/10*9.99 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 3
			end
		elseif e.y < (OPTIONS.screen_height*GLOBAL.zoom/10 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 4
			if e.y < (OPTIONS.screen_height*GLOBAL.zoom/20 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 5
			end
			if e.y < (OPTIONS.screen_height*GLOBAL.zoom/1000 - (OPTIONS.screen_height*GLOBAL.zoom/2)) + GLOBAL.moveup then
			GLOBAL.movingup = 6
			end
		else
			GLOBAL.movingup = 0
		end
    end
	if e["type"] == "onclick" and e["value"] then
        if e["value"] == "newmap" then
			OPTIONS.number_of_planets = g2.form.number_of_planets
			OPTIONS.max_production = g2.form.max_production
			OPTIONS.size_of_map = g2.form.size_of_map
            init_game();
            init_getready();
            g2.state = "pause"
		elseif (e["value"] == "restart") then
			main_menu()
        elseif (e["value"] == "resume") then
            g2.state = "play"
        elseif (e["value"] == "quit") then
            g2.state = "quit"
        end
    elseif e["type"] == "pause" then
        init_paused();
        g2.state = "pause"
    end
	
	if e.type == "ui:down" then
		if GLOBAL.clicked == 0 then
			GLOBAL.clicked = 1
			GLOBAL.countup = 0
		elseif GLOBAL.clicked == 1 and GLOBAL.lastex - 10 < e.x and GLOBAL.lastex + 10 > e.x and GLOBAL.lastey - 10 < e.y and GLOBAL.lastey + 10 > e.y then
			GLOBAL.clicked = 2
			GLOBAL.countup = 0
		elseif GLOBAL.clicked == 2 and GLOBAL.lastex - 10 < e.x and GLOBAL.lastex + 10 > e.x and GLOBAL.lastey - 10 < e.y and GLOBAL.lastey + 10 > e.y then
			GLOBAL.countup = 0
			local zoomnumber = tonumber(GLOBAL.zoom - 0.1)
			local size_of_map = tonumber(OPTIONS.size_of_map)
			if e.x > (OPTIONS.screen_width*GLOBAL.zoom/2 - (OPTIONS.screen_width*GLOBAL.zoom/2)) + GLOBAL.moveright then
				if zoomnumber > 2 then
					GLOBAL.zoom = GLOBAL.zoom - 1
				elseif zoomnumber <= 2 and zoomnumber >= 0.4 then
					GLOBAL.zoom = GLOBAL.zoom - 0.2
				end
			else
				if zoomnumber < size_of_map and zoomnumber > 1 then
					GLOBAL.zoom = GLOBAL.zoom + 1
				elseif zoomnumber < 1 and zoomnumber < size_of_map + 1 then
					GLOBAL.zoom = GLOBAL.zoom + 0.2
				end
			end
			zoomlbl.label_text = "Zoom: "..GLOBAL.zoom - 0.1
		end
		GLOBAL.lastex = e.x
		GLOBAL.lastey = e.y
    end
end

-- no need to touch this
function init_game()
	
    g2.game_reset();
    
	GLOBAL.clicked = 0
	GLOBAL.moveright = 0
	GLOBAL.movingright = 0
	GLOBAL.moveup = 0
	GLOBAL.movingup = 0
	GLOBAL.countup = 0
	GLOBAL.zoom = OPTIONS.size_of_map + 0.1
	GLOBAL.linelist = {}
	GLOBAL.mousecircle = nil
	
    -- set up users
    local neutral = g2.new_user("neutral", 0x999999)
    neutral.user_neutral = 1
    neutral.ships_production_enabled = 0
	GLOBAL.neutral = neutral

    local player = g2.new_user("player", COLORS[4])
    g2.player = player
	GLOBAL.player = player
	
    local enemy = g2.new_user("bot", COLORS[5])
    GLOBAL.enemy = enemy
	
    -- generate map
    sk_mapGen(player, enemy, neutral)
    g2.planets_settle()
end